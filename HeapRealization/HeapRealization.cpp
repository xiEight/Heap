#include <iostream>
#include "Heap.h"

struct test
{
    bool operator()(int a, int b)
    {
        return a > b;
    }
};

int main()
{
    int N, x;
    std::cin >> N;
    Heap<int, test> heap(N);
    for (int i = 0; i < N; i++)
    {
        std::cin >> x;
        heap.insert(x);
    }
    for (auto x : heap)
        std::cout << x << " ";
    heap.remove(2);
    for (auto x : heap)
        std::cout << x << " ";
}

